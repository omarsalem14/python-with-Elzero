print(type(10))  # int => Integer
print(type(100))  # int => Integer
print(type(-10))  # int => Integer


print(type(100.24))  # float => Floating Point Number
print(type(2650.222))  # float => Floating Point Number
print(type(-100.2546))  # float => Floating Point Number


print(type("Hello,Python"))  # str => String


print(type([1, 2, 3, 4, 5, 6, 7, 8, 9]))  # list => List


print(type((1, 2, 3, 4, 5, 6, 7, 8, 9)))  # tuple => Tuple


print({"one": 1, "two": 2, "three": 3})  # dict => Dictionary


print(2 == 2)  # bool =>Boolean Value:True
print(2 == 4)  # bool =>Boolean Value:False
